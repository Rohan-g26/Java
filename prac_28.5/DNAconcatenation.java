public class DNAconcatenation 
{
    public static void main(String[] args) 
   {

        String dna1 = "ATTCGCTAGA";
        String dna2 = "GCATTACC";

        System.out.println("dna1 = " + dna1);
        System.out.println("dna2 = " + dna2 + '\n');

        System.out.println("dna1 + dna2 = " + (dna1 + dna2));

    }
}